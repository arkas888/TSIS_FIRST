from functools import reduce

def multiply_list(numbers):
    # Используем встроенную функцию reduce для умножения всех элементов списка
    result = reduce(lambda x, y: x * y, numbers)
    return result

def main():
    # Пример списка чисел
    my_numbers = [2, 3, 4, 5, 6]

    # Вызываем функцию для умножения всех чисел в списке
    result = multiply_list(my_numbers)

    # Выводим результат
    print(f"Произведение всех чисел в списке: {result}")

if __name__ == "__main__":
    main()
    
'''from functools import reduce: Мы импортируем функцию reduce из модуля functools. functools является частью стандартной библиотеки Python.

multiply_list(numbers): Это функция, которая принимает список чисел в качестве аргумента. Внутри используется встроенная функция reduce.

result = reduce(lambda x, y: x * y, numbers): Мы используем функцию reduce, передавая ей лямбда-функцию 
lambda x, y: x * y и список чисел numbers. Функция reduce последовательно применяет лямбда-функцию к элементам списка, умножая их.

return result: Функция возвращает результат умножения всех чисел в списке.

my_numbers = [2, 3, 4, 5, 6]: Пример списка чисел, который мы передаем функции multiply_list.

result = multiply_list(my_numbers): Мы вызываем функцию multiply_list с нашим списком чисел и сохраняем результат в переменной result.

print(f"Произведение всех чисел в списке: {result}"): Выводим результат умножения чисел на экран.

if __name__ == "__main__": main(): Этот блок проверяет, является ли данный скрипт основным (главным) исполняемым файлом.
Если да, то вызывается функция main().

Таким образом, программа умножает все числа в заданном списке с использованием встроенной функции reduce.'''