import time
import math

def delayed_square_root(number, delay_ms):
    # Засыпаем на указанное количество миллисекунд
    time.sleep(delay_ms / 1000.0)

    # Вызываем встроенную функцию извлечения квадратного корня
    result = math.sqrt(number)

    return result

def main():
    # Вводим число и задержку от пользователя
    number = float(input("Введите число для извлечения квадратного корня: "))
    delay_ms = int(input("Введите количество миллисекунд задержки: "))

    # Вызываем функцию для извлечения квадратного корня с задержкой
    result = delayed_square_root(number, delay_ms)

    # Выводим результат
    print(f"Квадратный корень из {number} после {delay_ms} миллисекунд: {result}")

if __name__ == "__main__":
    main()
    
'''Создаем функцию delayed_square_root(number, delay_ms), которая принимает число и количество миллисекунд задержки.

Используем встроенную функцию time.sleep(delay_ms / 1000.0), чтобы заснуть на указанное количество миллисекунд. 
Разделим на 1000.0, чтобы преобразовать миллисекунды в секунды.

После задержки вызываем встроенную функцию math.sqrt() для извлечения квадратного корня от числа.

В функции main() запрашиваем у пользователя ввод числа и количество миллисекунд задержки.

Вызываем функцию delayed_square_root(number, delay_ms) для извлечения квадратного корня с задержкой.

Выводим результат.'''