def histogram(numbers):
    for number in numbers:
        print('*' * number)

# Example of using the function:
histogram([4, 9, 7])

'''Ниже представлена функция на Python под названием histogram, которая принимает список целых чисел и выводит гистограмму на экран:

1. Определена функция histogram с параметром numbers, представляющим список целых чисел.
2. В цикле for перебираются числа из списка.
3. Для каждого числа выводится строка, состоящая из символа '*' повторенного число раз.

Пример использования функции:
histogram([4, 9, 7])
Эта функция принимает список целых чисел и для каждого числа выводит гистограмму на экран в виде строк, 
где количество звездочек соответствует значению числа в списке.'''